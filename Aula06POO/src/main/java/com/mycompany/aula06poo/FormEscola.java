    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (objProf != null){
            objProf.setCpf(JOptionPane.showInputDialog("Cpf do Professor:"));
            objProf.setNome(JOptionPane.showInputDialog("Nome do Professor:"));
            
            int numIdioma = Integer.parseInt(JOptionPane.showInputDialog("1 - Inglês | 2 - Espanhol:"));            
            if (numIdioma == 1) 
                objProf.setIdioma(idioma1); 
            else 
                objProf.setIdioma(idioma2);
            
            objProf.setChSemanal(Double.parseDouble(JOptionPane.showInputDialog("Carga horária semanal:")));
            objProf.setValorHora(Double.parseDouble(JOptionPane.showInputDialog("Valor da hora:")));
            JOptionPane.showMessageDialog(null, objProf.toString());
        }
        else{
            JOptionPane.showMessageDialog(null, "Objeto não foi criado!!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

